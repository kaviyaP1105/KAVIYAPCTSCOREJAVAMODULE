import java.util.*;
import java.lang.*;
class  OperatorPrecedence{
    public static void main(String[] args) {
        int result=10 + 5 * 2;
        System.out.println(result);
        System.out.println("First 5*2 is evaluated = "+(5*2));
        System.out.println("Then 10 is added to 10 = "+(10+10));
        
    }
}